# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def removeNthFromEnd(self, head: Optional[ListNode], n: int) -> Optional[ListNode]:
        temp = head
        length = 0
        while temp:
            temp = temp.next
            length += 1
        
        remove_idx = length - n

        if remove_idx == 0:
            return head.next

        curr = head
        for i in range(length):
            if i + 1 == remove_idx:
                curr.next = curr.next.next
                break
            curr = curr.next

        return head

        
